int		ft_count_if(char **tab, int (*f)(char*))
{
	int pos;
	int count;

	pos = 0;
	count = 0;
	while (tab[pos])
	{
		if (f(tab[pos]) == 1)
			count++;
		pos++;
	}
	return (count);
}
