int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int str;

	str = 0;
	while (str < length - 1)
	{
		if (f(tab[str], tab[str + 1]) > 0)
			return (0);
		str++;
	}
	return (1);
}
